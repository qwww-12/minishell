#include "libft.h"

void	ft_strncpy(char *s1, char *s2, int n)
{
	int	i;

	if (!s1 || !s2)
		return ;
	i = 0;
	while (s2[i] && i < n)
	{
		s1[i] = s2[i];
		i++;
	}
	while (i < n)
		s1[i++] = '\0';
}
